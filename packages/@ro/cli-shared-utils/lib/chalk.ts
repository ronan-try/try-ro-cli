import chalk from 'chalk';

export const textRed = (msg: string) => chalk.red(msg);

export const textCyan = (msg: string)=> chalk.cyan(msg);

export const textYellow = (msg: string) => chalk.yellow(msg);

export const textGreen = (msg: string) => chalk.green(msg);

export const textRedBright = (msg: string) => chalk.redBright(msg);

export const textCyanBright = (msg: string) => chalk.cyanBright(msg);

export const textGray = (msg: string) => chalk.gray(msg);
